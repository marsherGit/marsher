package spring.message;

import java.util.Date;

public class ReceiveMsg {
	private int re_num;
	private String re_title;
	private String re_content;
	private String re_receiver;
	private String re_sender;
	private Date re_regdate;
	private Date re_checkDate;
	private String st_id;
	public int getRe_num() {
		return re_num;
	}
	public void setRe_num(int re_num) {
		this.re_num = re_num;
	}
	public String getRe_title() {
		return re_title;
	}
	public void setRe_title(String re_title) {
		this.re_title = re_title;
	}
	public String getRe_content() {
		return re_content;
	}
	public void setRe_content(String re_content) {
		this.re_content = re_content;
	}
	public String getRe_receiver() {
		return re_receiver;
	}
	public void setRe_receiver(String re_receiver) {
		this.re_receiver = re_receiver;
	}
	public String getRe_sender() {
		return re_sender;
	}
	public void setRe_sender(String re_sender) {
		this.re_sender = re_sender;
	}
	public Date getRe_regdate() {
		return re_regdate;
	}
	public void setRe_regdate(Date re_regdate) {
		this.re_regdate = re_regdate;
	}
	public Date getRe_checkDate() {
		return re_checkDate;
	}
	public void setRe_checkDate(Date re_checkDate) {
		this.re_checkDate = re_checkDate;
	}
	public String getSt_id() {
		return st_id;
	}
	public void setSt_id(String st_id) {
		this.st_id = st_id;
	}
	
	

}
