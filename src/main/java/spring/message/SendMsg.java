package spring.message;

import java.util.Date;

public class SendMsg {
	private int num;
	private String se_title;
	private String se_content;
	private String se_receiver;
	private String se_sender;
	private Date se_regdate;
	private Date se_checkDate;
	private String st_id;
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public String getSe_title() {
		return se_title;
	}
	public void setSe_title(String se_title) {
		this.se_title = se_title;
	}
	public String getSe_content() {
		return se_content;
	}
	public void setSe_content(String se_content) {
		this.se_content = se_content;
	}
	public String getSe_receiver() {
		return se_receiver;
	}
	public void setSe_receiver(String se_receiver) {
		this.se_receiver = se_receiver;
	}
	public String getSe_sender() {
		return se_sender;
	}
	public void setSe_sender(String se_sender) {
		this.se_sender = se_sender;
	}
	public Date getSe_regdate() {
		return se_regdate;
	}
	public void setSe_regdate(Date se_regdate) {
		this.se_regdate = se_regdate;
	}
	public Date getSe_checkDate() {
		return se_checkDate;
	}
	public void setSe_checkDate(Date se_checkDate) {
		this.se_checkDate = se_checkDate;
	}
	public String getSt_id() {
		return st_id;
	}
	public void setSt_id(String st_id) {
		this.st_id = st_id;
	}
	
	
}
